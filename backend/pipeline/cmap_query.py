class CMAPQuery:
    """
    CMap query engine for ATRT pipeline v3.0.

    Two modes:
      1. Pre-computed (primary): loads scores from JSON generated by
         integrate_cmap_results.py after clue.io query submission.
         Source: data/cmap_query/atrt_cmap_scores.json

      2. Local .gctx (fallback): requires full LINCS dataset (~30 GB).
         Use clue.io web interface instead for convenience.

    When no data is available, returns neutral prior (0.50) with logging.
    NEVER returns hardcoded connectivity scores.
    """

    def __init__(self, data_dir: str = "data/raw_omics/"):
        self.gctx_path    = Path(data_dir) / "level5_beta_trt_cp_n720216x12328.gctx"
        self.siginfo_path = Path(data_dir) / "siginfo_beta.txt"
        self.is_ready     = False
        self.drug_to_sig_map: Dict[str, List[str]] = {}
        self._precomputed: Dict[str, Dict] = {}
        self._data_source: str = "none"

        self._load_precomputed()

        if not CMAP_AVAILABLE:
            logger.debug("cmapPy not installed — local .gctx mode unavailable")

        if self._precomputed:
            logger.info(
                "✅ CMap ATRT: %d pre-computed scores from %s",
                len(self._precomputed), self._data_source,
            )
        elif self.gctx_path.exists():
            logger.info("CMap: .gctx file found — local mode available")
        else:
            logger.info(
                "CMap: no ATRT scores loaded. Returning neutral priors (0.50).\n"
                "To add live scores:\n"
                "  1. python scripts/01_prepare_cmap.py\n"
                "  2. Submit gene lists to https://clue.io\n"
                "  3. python -m backend.pipeline.integrate_cmap_results"
            )

    def _load_precomputed(self) -> None:
        """Load ATRT-specific pre-computed clue.io scores from JSON."""
        if ATRT_PRECOMPUTED_SCORES_PATH.exists():
            try:
                with open(ATRT_PRECOMPUTED_SCORES_PATH) as f:
                    self._precomputed = json.load(f)
                self._data_source = str(ATRT_PRECOMPUTED_SCORES_PATH)
                logger.info(
                    "CMap: loaded ATRT scores from %s", self._data_source
                )
                return
            except Exception as e:
                logger.warning("Failed to load ATRT CMap scores: %s", e)

        # No ATRT scores available yet
        logger.info(
            "CMap: %s not found. "
            "Run scripts/01_prepare_cmap.py then integrate_cmap_results.py "
            "to generate ATRT-specific scores.",
            ATRT_PRECOMPUTED_SCORES_PATH,
        )

    def get_precomputed_score(self, drug_name: str) -> float:
        """
        Return pre-computed clue.io cmap_score for a drug.
        Returns neutral prior (0.50) if drug not profiled or not in results.
        Never returns hardcoded connectivity scores.
        """
        if not self._precomputed:
            return 0.50  # Neutral prior — no data loaded

        name_upper = drug_name.upper().strip()

        # Check L1000 library notes first
        lib_note = L1000_LIBRARY_NOTES.get(name_upper)
        if lib_note and not lib_note["in_library"]:
            logger.debug(
                "CMap: %s not in L1000 library (%s) — neutral prior",
                drug_name, lib_note["reason"],
            )
            return 0.50

        # Try exact match
        if name_upper in self._precomputed:
            return self._precomputed[name_upper].get("cmap_score", 0.50)

        # Try CMap alias (e.g. BIRABRESIB → OTX-015)
        if lib_note and lib_note.get("cmap_alias"):
            alias = lib_note["cmap_alias"].upper()
            if alias in self._precomputed:
                return self._precomputed[alias].get("cmap_score", 0.50)

        # Try partial match (handles salt forms, minor name variants)
        for key in self._precomputed:
            if key in name_upper or name_upper in key:
                return self._precomputed[key].get("cmap_score", 0.50)

        return 0.50  # Drug not profiled — neutral prior

    def has_precomputed_scores(self) -> bool:
        return bool(self._precomputed)

    def get_data_provenance(self) -> str:
        """Return description of data source for logging/reporting."""
        if not self._precomputed:
            return (
                "No CMap ATRT scores loaded. All scores = neutral prior (0.50). "
                "Run scripts/01_prepare_cmap.py → submit to clue.io → "
                "integrate_cmap_results.py to add real scores."
            )
        n_reversers = sum(
            1 for d in self._precomputed.values()
            if d.get("is_reverser")
        )
        return (
            f"ATRT CMap scores from: {self._data_source} "
            f"({len(self._precomputed)} drug profiles, "
            f"{n_reversers} SMARCB1-loss reversers)"
        )

    async def _load_metadata(self) -> None:
        """Load .gctx metadata for local mode (only if file exists)."""
        if self.is_ready or not self.gctx_path.exists() or not self.siginfo_path.exists():
            return
        logger.info("Loading CMap .gctx metadata (local mode)...")
        siginfo = pd.read_csv(self.siginfo_path, sep="\t", low_memory=False)
        hq_sigs = siginfo[siginfo["is_gold"] == 1]
        for _, row in hq_sigs.iterrows():
            drug_name = str(row["cmap_name"]).upper()
            sig_id    = row["sig_id"]
            if drug_name not in self.drug_to_sig_map:
                self.drug_to_sig_map[drug_name] = []
            self.drug_to_sig_map[drug_name].append(sig_id)
        self.is_ready = True
        logger.info("CMap .gctx metadata: %d drugs", len(self.drug_to_sig_map))

    def _norm_cs_to_pipeline_score(self, norm_cs: float) -> float:
        """
        Convert norm_cs [-2, +2] to pipeline cmap_score [0, 1].
        norm_cs = -2.0 → 1.00 (perfect reversal of ATRT SMARCB1-loss signature)
        norm_cs =  0.0 → 0.50 (neutral)
        norm_cs = +2.0 → 0.00 (mimics ATRT signature)
        """
        scale = CMAP_CONFIG.get("norm_cs_to_score_scale", 4.0)
        score = (2.0 - norm_cs) / scale
        return round(max(0.05, min(0.95, score)), 4)

    def _calculate_reversal_score(
        self, drug_expr: pd.Series, disease_sig: Dict
    ) -> float:
        """Calculate reversal score for local .gctx mode."""
        score = 0.0
        valid = 0
        for gene in disease_sig.get("up", []):
            if gene in drug_expr.index:
                score -= drug_expr[gene]
                valid += 1
        for gene in disease_sig.get("down", []):
            if gene in drug_expr.index:
                score += drug_expr[gene]
                valid += 1
        if valid == 0:
            return 0.0
        avg = score / valid
        return float(1 / (1 + np.exp(-avg)))

    async def query_reversers(self, disease: str, top_k: int = 50) -> List[Dict]:
        """
        Return top drug reversers of the ATRT SMARCB1-loss signature.
        Uses pre-computed scores if available, otherwise local .gctx mode.
        """
        if self._precomputed:
            reversers = sorted(
                self._precomputed.items(),
                key=lambda x: x[1].get("cmap_score", 0),
                reverse=True,
            )[:top_k]
            threshold = CMAP_CONFIG.get("reversal_threshold", -0.90)
            return [
                {
                    "name":       name,
                    "cmap_score": data.get("cmap_score", 0.50),
                    "norm_cs":    data.get("norm_cs"),
                    "is_reverser": (
                        data.get("norm_cs", 0) < threshold
                        if data.get("norm_cs") is not None else False
                    ),
                    "source":     self._data_source,
                }
                for name, data in reversers
            ]

        # Local .gctx fallback
        await self._load_metadata()
        if not self.is_ready:
            logger.warning(
                "No CMap data available. Returning empty reverser list. "
                "All drugs will receive neutral CMap score (0.50). "
                "Run scripts/01_prepare_cmap.py to generate real scores."
            )
            return []

        return []  # Full local .gctx parsing would go here if needed